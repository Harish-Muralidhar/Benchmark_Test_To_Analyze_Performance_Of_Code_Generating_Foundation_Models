def find_max(s):
    max_num = 0
    ind = 0
    for i in range(len(s)):
        if s[i].isdigit():
            for j in range(i + 1, len(s)):
                if s[j].isdigit():
                    num = int(s[i:j + 1])
                    if num > max_num:
                        max_num = num
                        ind = i
    return max_num, ind

def find_max_change(s):
    max_num, ind = find_max(s)
    s = list(s)
    for i in range(ind, len(s)):
        if s[i].isdigit():
            for j in range(i + 1, len(s)):
                if s[j].isdigit():
                    if int(s[i:j + 1]) > max_num:
                        s[i] = '9'
                        max_num = int(''.join(s[i:j + 1]))
                        break
    return max_num

